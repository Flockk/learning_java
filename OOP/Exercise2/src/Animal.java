public abstract class Animal {

    private String size;
    private int age;

    public Animal(String size, int age) {
        this.size = size;
        this.age = age;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
